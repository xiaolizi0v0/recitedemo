package pojo;

public class User {

	int id;
	String userName;
	int planId; 
	int spellingListId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getPlanId() {
		return planId;
	}
	public void setPlanId(int planId) {
		this.planId = planId;
	}
	public int getSpellingListId() {
		return spellingListId;
	}
	public void setSpellingListId(int spellingListId) {
		this.spellingListId = spellingListId;
	}
	
}
